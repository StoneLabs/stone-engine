		Matrix4f::Matrix4f()
		{
			for (int i = 0; i < MATRIX_LENGTH; i++)
				elements[i] = 0.0f;
		}
		Matrix4f::Matrix4f(float diagonal)
		{
			for (int i = 0; i < MATRIX_LENGTH; i++)
				elements[i] = i % (MATRIX_EDGE_LENGTH + 1) ? 0 : diagonal;
		}
